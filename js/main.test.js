const { getSum } = require('./main');

describe('getSum', () => {
  test('возвращает строку с описанием и суммой двух положительных чисел', () => {
    expect(getSum(2, 3)).toBe('Сумма двух чисел равна 5');
  });

  test('возвращает строку с описанием и суммой отрицательных чисел', () => {
    expect(getSum(-5, -3)).toBe('Сумма двух чисел равна -8');
  });

  test('возвращает строку с описанием и суммой чисел с разными знаками', () => {
    expect(getSum(-10, 5)).toBe('Сумма двух чисел равна -5');
  });

  test('возвращает строку с описанием и суммой, когда одно число ноль', () => {
    expect(getSum(0, 7)).toBe('Сумма двух чисел равна 7');
  });

  test('возвращает строку с описанием и суммой дробных чисел', () => {
    expect(getSum(1.5, 2.5)).toBe('Сумма двух чисел равна 4');
  });
});
